func createDictionary(weight: Int, height: Int) -> [String : Int] {
    
    var dict = [String : Int]()
    
    for i in -1 ... weight {
        for j in -1 ... weight {
            if i == -1 || j == -1 || i == weight || j == height {
                dict["\(i):\(j)"] = 0 // black
            } else {
                dict["\(i):\(j)"] = 1 // white
            }
        }
    }
    
    return dict
}

